     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->all();
        $event = new Event();
        $interval = '';

        for ($i = 0; $i < sizeof($data['interval']); $i++) {
            $interval .= $data['interval'][$i] . ',';
        }


        $event->create(['start' => $data['start'], 'end' => $data['end'], 'name' => $data['name'], 'interval' => $interval,]);
    }

